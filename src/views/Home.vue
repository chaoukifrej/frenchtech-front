<template>
  <div id="home">
    <Header />
    <MetricsHome />
    <div class="mainContainer">
      <!-- Carte Taille et Modif -->
      <l-map class="map" :zoom="zoom" :center="center">
        <!-- Rappel url openstreetmap -->
        <l-tile-layer :url="url"></l-tile-layer>
        <l-geo-json
          :optionsStyle="geoJsonStyle"
          :geojson="geojson"
        ></l-geo-json>
        <!-- Marqueur Carte-->
        <l-marker
          v-for="elem in actors"
          :key="elem.id"
          :lat-lng="[elem.longitude, elem.latitude]"
        >
          <l-tooltip>{{ elem.name }}</l-tooltip>
          <l-icon>
            <!-- <img class="markerPin" src="img/pin-point.png" /> -->
            <b-icon
              class="rounded-circle bg-danger p-1"
              icon="circle-fill"
              variant="light"
              animation="throb"
            ></b-icon>
          </l-icon>
        </l-marker>
      </l-map>

      <div class="blocCards">
        <div class="displayCards" v-for="item in actors" :key="item.id">
          <CardInfo :i="item" />
        </div>
      </div>
    </div>
  </div>
</template>

<script>
/* Importation components */
import {
  LMap,
  LTileLayer,
  LMarker,
  LTooltip,
  LIcon,
  LGeoJson,
} from "vue2-leaflet";
import "leaflet-defaulticon-compatibility/dist/leaflet-defaulticon-compatibility.webpack.css";
import "leaflet-defaulticon-compatibility";

// Import header et CardInfo
import Header from "@/components/Header.vue";
import CardInfo from "@/components/CardInfo.vue";
//Metriques
import MetricsHome from "@/components/MetricsHome.vue";

export default {
  name: "App",
  inject: ["baseUrl"],
  components: {
    LMap,
    LTileLayer,
    LMarker,
    LTooltip,
    LIcon,
    LGeoJson,
    Header,
    CardInfo,
    MetricsHome,
  },
  data() {
    return {
      /* url Carte */
      url: "https://tiles.stadiamaps.com/tiles/alidade_smooth/{z}/{x}/{y}.png",

      center: [43.70496267989356, 7.271699366104521],
      /* Zoom de la carte*/
      zoom: 11,

      //GeoZone
      geojson: null,
      geoJsonStyle: {
        color: "rgba(0, 0, 0, 0.3)",
        weight: 1,
      },
      /* Centrage CoordonnÃ©es*/
      actors: [],
    };
  },

  async created() {
    const response = await fetch(
      "https://rawgit.com/gregoiredavid/france-geojson/master/departements/06-alpes-maritimes/departement-06-alpes-maritimes.geojson"
    );
    this.geojson = await response.json();
  },
  mounted() {
    /* mounted pour recuperer les infos des Actors depuis la BDD */
    this.axios

      .get(this.baseUrl + "api/GET/actors")

      .then((response) => {
        for (const elem of response.data.body.actors) {
          this.actors.push(elem);
        }
        //console.log(this.actors);
      });
  },

  methods: {},
};
</script>

